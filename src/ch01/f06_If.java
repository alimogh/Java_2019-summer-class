package ch01;

/*
 * 	사용법
 * 	if(조건식){
 * 		여러 명령어
 * 	}
 */

public class f06_If {

	public static void main(String[] args) {
		
		int score = 70;
		if(score >= 80) {
			System.out.println("축하합니다!");
			System.out.println("합격하셨습니다.");
		}
		System.out.println("수고하셨습니다.");
		
	}

}
